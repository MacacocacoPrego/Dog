package entidades;

public class Dono {
	
	int id;
	int cpf;
	String name;
	int idade;
	int numeroCel;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getNumeroCel() {
		return numeroCel;
	}
	public void setNumeroCel(int numeroCel) {
		this.numeroCel = numeroCel;
	}
	
}
