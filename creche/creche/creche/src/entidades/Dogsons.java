package entidades;

public class Dogsons {
	int id;
	String raça;
	boolean sexo;
	int idade;
	
	public int getIdDog() {
		return id;
	}
	public void setIdDog(int idDog) {
		this.id = idDog;
	}
	public String getRaça() {
		return raça;
	}
	public void setRaça(String raça) {
		this.raça = raça;
	}
	public boolean isSexo() {
		return sexo;
	}
	public void setSexo(boolean sexo) {
		this.sexo = sexo;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	

}
